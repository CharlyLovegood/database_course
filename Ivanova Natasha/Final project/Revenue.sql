WITH RECURSIVE cte AS
(
    SELECT MIN(CAST(dttm AS DATE)) AS dt FROM visits
        UNION ALL
    SELECT dt + INTERVAL 1 DAY
        FROM cte
    WHERE dt + INTERVAL 1 DAY <= (SELECT MAX(CAST(dttm AS DATE)) FROM visits)
),
REVENUE AS
(
    SELECT CAST(dttm AS DATE) AS dt, SUM(orders.total_price) as count
        FROM orders
        GROUP BY dt
)
SELECT cte.dt as day, REV.count AS Revenue
    FROM cte
    LEFT JOIN REVENUE AS REV ON cte.dt = REV.dt


